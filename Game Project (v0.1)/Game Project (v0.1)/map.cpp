//Adding map materials here
Map::Map()
{
	backgroundTexture = TextureManager::LoadTexture("back_texture1(128).png");
	//asteroid = TextureManager::LoadTexture("asteroid.png");

	LoadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 128;
	src.h = dest.h = 128;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[40][20])
{
	for (int row = 0; row < 40; row++)
	{
		for (int column = 0; column < 20; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;


	for (int row = 0; row < 40; row++)
	{
		for (int column = 0; column < 20; column++)
		{
			type = map[row][column];

			dest.x = column * 128; 
			dest.y = row * 128; 

			switch (type)
			{
			case 0:
				TextureManager::Draw(backgroundTexture, src, dest);
				break;
			default:
				break;
			}

		}
	}
}